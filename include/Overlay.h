#pragma once
#include <windows.h>

// Глобальные переменные, используемые для управления оверлейным окном выделения:

// Дескриптор окна оверлея, в котором рисуется выделенная область.
extern HWND overlayWindow;       

// Точка, с которой началось выделение (обычно соответствует позиции курсора при нажатии правой кнопки мыши).
extern POINT startPoint;         

// Текущая позиция курсора в процессе выделения.
extern POINT currentPoint;       

// Флаг, указывающий, что процесс выделения активен.
extern bool selecting;           

// Границы монитора, на котором происходит выделение.
// Используется для ограничения выделенной области в пределах экрана.
extern RECT monitorBounds;       

// Создает оверлейное окно для выделения.
// Параметры:
// - hInstance: дескриптор модуля, необходимый для создания окна.
// - anchor: точка привязки, обычно это стартовая позиция выделения.
void CreateOverlayWindow(HINSTANCE hInstance, POINT anchor);

// Уничтожает оверлейное окно, завершая процесс выделения.
void DestroyOverlayWindow();
